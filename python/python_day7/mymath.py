import math
class mymath1:
    def triangle(self, num1, num2):
        answer = (num1*num2)/2
        return answer

    def circle(self, num1):
        answer = (num1*num1*math.pi)
        return answer

    def cube(self, num1, num2, num3):
        answer = num1*num2*num3
        return answer

    


